void shellsort(int n, int array[])
{
    int i, j, x, d;
    
	d = d / 2;
	
	while(d > 0)
	{
		i = d;
		while(i < n)
		{
			x = array[i];
			j = i;
			while(j >= d)
			{
				if(x > array[j - d])
				{
					array[j] = array[j - d];
					j = j - d;
				}
				else break;
			}
			array[i] = x;
		}
		d = d / 2;
	}
}

void forshellsort(int n, int array[])
{
    int i, j, d;
    int tmp;
    
    for (d = n / 2; d > 0; d /= 2)
    {
        for (i = d; i < n; i++)
        {
            tmp = array[i];
            for (j = i; j >= d; j -= d)
            {
                if (tmp < array[j - d])
                    array[j] = array[j - d];
                else
                    break;
            }
            array[j] = tmp;
    	}
	}
}

